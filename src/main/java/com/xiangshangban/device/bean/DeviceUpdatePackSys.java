package com.xiangshangban.device.bean;

public class DeviceUpdatePackSys {
    private String newSysVerion;

    private String path;

    private String createTime;

    private String fileKey;

    public String getNewSysVerion() {
        return newSysVerion;
    }

    public void setNewSysVerion(String newSysVerion) {
        this.newSysVerion = newSysVerion;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getCreateTime() {
        return createTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public String getFileKey() {
        return fileKey;
    }

    public void setFileKey(String fileKey) {
        this.fileKey = fileKey;
    }
}