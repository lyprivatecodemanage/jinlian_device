package com.xiangshangban.device.bean;

public class TimeRangePrivilegeEmployee {
    private String employeeId;

    private String dayOfWeek;

    private String rangeStartTime;

    private String rangeEndTime;

    private String rangeDoorOpenType;

    private String rangeFlagId;

    private String doorId;

    public String getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(String employeeId) {
        this.employeeId = employeeId;
    }

    public String getDayOfWeek() {
        return dayOfWeek;
    }

    public void setDayOfWeek(String dayOfWeek) {
        this.dayOfWeek = dayOfWeek;
    }

    public String getRangeStartTime() {
        return rangeStartTime;
    }

    public void setRangeStartTime(String rangeStartTime) {
        this.rangeStartTime = rangeStartTime;
    }

    public String getRangeEndTime() {
        return rangeEndTime;
    }

    public void setRangeEndTime(String rangeEndTime) {
        this.rangeEndTime = rangeEndTime;
    }

    public String getRangeDoorOpenType() {
        return rangeDoorOpenType;
    }

    public void setRangeDoorOpenType(String rangeDoorOpenType) {
        this.rangeDoorOpenType = rangeDoorOpenType;
    }

    public String getRangeFlagId() {
        return rangeFlagId;
    }

    public void setRangeFlagId(String rangeFlagId) {
        this.rangeFlagId = rangeFlagId;
    }

    public String getDoorId() {
        return doorId;
    }

    public void setDoorId(String doorId) {
        this.doorId = doorId;
    }
}